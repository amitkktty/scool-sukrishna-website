                                                <ul>
                                                    <li>Enroll after Class 12</li>
                                                    <li>Appear for CSEET, which is held four times a year</li>
                                                    <li>Qualify for CSEET to move to the CS Executive level</li>
                                                </ul>

                                                <p><strong>Step 2: Register for CS Executive</strong></p>
                                                <ul>
                                                    <li>After clearing CSEET OR</li>
                                                    <li>Direct Entry after Graduation (exempt from CSEET)</li>
                                                    <li>Complete Executive Program (two modules)</li>
                                                </ul>

                                                <p><strong>Step 3: Register for CS Professional</strong></p>
                                                <ul>
                                                    <li>After clearing CSEET OR</li>
                                                    <li>Direct Entry after Graduation (exempt from CSEET)</li>
                                                    <li>Complete Executive Program (two modules)</li>
                                                </ul>

